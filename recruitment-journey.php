<script>
    var sections = $('section')
        , navs = $('navs')
        , navs_height = navs.outerHeight();

    $(window).on('scroll', function () {
        var cur_pos = $(this).scrollTop();

        sections.each(function () {
            var top = $(this).offset().top - navs_height,
                bottom = top + $(this).outerHeight();

            if (cur_pos >= top && cur_pos <= bottom) {
                navs.find('a').removeClass('active');
                sections.removeClass('active');

                $(this).addClass('active');
                navs.find('a[href="#' + $(this).attr('id') + '"]').addClass('active');
            }
        });
    });

    navs.find('a').on('click', function () {
        var $el = $(this)
            , id = $el.attr('href');

        $('html, body').animate({
            scrollTop: $(id).offset().top - navs_height
        }, 500);

        return false;
    });
</script>