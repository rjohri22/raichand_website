                                02. What are International Commercial Terms (Incoterms)?
                            </label>
                            <div class="contents">
                                 <div class="text">
                                     The seven terms for all modes of transport (Air, Sea and Inland)
                                        are:<br>
                                        <b>EXW</b> - EX Works<br>
                                        <b>FCA</b> - Free Carrier<br>
                                        <b> CPT</b> - Carriage Paid To<br>
                                        <b>CIP</b> - Carriage and Insurance Paid to<br>
                                        <b>DAT</b> - Delivered At Terminal<br>
                                        <b>DAP</b> –Delivered At Place<br>
                                        <b>DDP</b> - Delivered Duty Paid<br>
                                        <br>
                                        The four terms for sea and inland waterway transport are:
                                        <b>FAS </b>- Free Along- side Ship<br>
                                        <b>FOB </b>- Free On Board<br>
                                        <b>CFR</b> - Cost and Freight<br>
                                        <b>CIF </b>- Cost Insurance and Freight<br>

