                                03. What are shipping modes?
                            </label>
                            <div class="contents">
                                <div class="text"><strong>Sea Transport</strong> - There are many shipping modes
                                    such as Bulk Cargo,
                                    Break Bulk Cargo, Project Cargo, Container Cargo, RORO (roll-on/rolloff), etc.
                                    The most common mode among them is container cargo
                                    shipping.<br><br>
                                    <strong>Air Transport</strong> - Air transport can prove to be cost-effective
                                    for low
                                    weight and small volume shipments. Air Cargo has strict policies on the
                                    packaging of the goods and an exporter needs to adhere by International
                                    Air Transport Association (IATA) guidelines. Air transport is the
                                    fastest among the transportation modes and is commonly used for
                                    exporting perishable goods having a short shelf life. We packages the
                                    cargo in box- es, bags etc. and the airlines carrier loads them into Unit
                                    Load Devices (ULD).<br><br>

                                    <strong>Land Transport</strong> - Land transport is a very appealing mode of
                                    transportation for exporting as the transit time is lower than in case of
                                    sea trans- port and the costs are lower compared to air transport.
                                    Different types of trucks are used for land transport based on the nature
                                    of the export product.<br>

                                    <br>The most popular ones are given below:<br>
                                    <ul class="ml-65">
                                        <li> <strong>Palletized Cargo on Flatbed Trucks</strong> : Flatbed trucks
                                            are usually
                                            used for transporting non-perishable dry cargo products. These
                                            are packed into boxes, bags etc. and palletized by machines for
                                            easy loading and unloading.<br><br></li>

                                        <li> <strong>Perishable Cargo</strong> : like non-temperature sensitive food
                                            products,
                                            which needs to be protected from dust, sun, etc. is usually
                                            palletized and transported in a boxed or covered trailers.</li>

                                        <li><strong>Temperature Sensitive Cargo</strong> : Food products like meat,
                                            dairy
                                            products, etc. and medicines are palletized and transported in
                                            refrigerated trucks to maintain their temperature requirements.<br><br>
                                        </li>
                                        <li></li>

                                    </ul>


                                    <strong>Rail Transport</strong> - Rail transport is typically used as an
                                    intermediate
                                    transportation mode with land or sea shippers usually aiding in the
                                    completion of the delivery. Land and/or sea transport may be used to
                                    deliver an export product from the exporter’s location to an
                                    intermediate rail terminal.

                                </div>
                            </div>
                        </div>

                        <div class="options">
                            <input type="checkbox" id="toggle5" class="toggle">
                            <label class="titles" for="toggle5">
                                04. Who are logistics service providers?
                            </label>
                            <div class="contents">
                                <div class="text">There are four main types of logistics service providers that can
                                    provide
                                    support in exporting:
                                    <ul>
                                        <li><strong>a. Carriers</strong> - Carriers are the companies that own and
                                            operate the
                                            means of transport used to move shipments across borders
                                            between buyers and sellers. (Shipping lines, air lines, trucking
                                            companies, etc.). <br>
                                            <div class="ml-65">
                                                <strong>Shipping lines</strong> are organizations or individual
                                                owners of
                                                different types of ships. Large customers with large
                                                volumes of export goods directly contract with the
                                                shipping lines whereas smaller ones usually use shipping
                                                agents / freight forwarders to book a vessel.<br>

                                                <strong>Airlines</strong> are either government or private companies
                                                which own the aeroplanes used for air freight.<br>

                                                <strong>Trucking companies</strong> usually arrange the movement of
                                                the shipment from the exporter’s factory or warehouse to
                                                the carrier departure port and from the carrier arrival port
                                                to the buyer’s warehouse.
                                            </div>
                                        </li>
                                        <li><strong>b. Freight Forwarders</strong> - A freight forwarder is a
                                            service provider
                                            who arranges the import and export processes with the logistics
                                            service provider on behalf of the seller or buyer. The freight
                                            forwarder does not actually move the cargo and, most probably,
                                            does not own any vessels or airplanes. He only acts as a link
                                            between the exporter and various logistics service providers. A
                                            freight forwarder can handle all the shipping procedures and
                                            documentation starting from collecting the cargo from the
                                            seller’s warehouse to the delivery of the shipment to the buyer’s
                                            warehouse.</li>
                                        <li><strong>c. Customs Brokers (facilitators)</strong> - Customs brokers are
                                            licensed
                                            individuals who are authorized by Customs to perform the
                                            customs clearance procedures on behalf of exporters. Customs
                                            brokers must be given an authorization letter from the exporter
                                            along with the shipping documents to complete the clearance
                                            procedures. A customs broker who is licensed in the port they
                                            are exporting from, as each port has a separate customs
                                            brokering license. In addition, an exporter must make sure that
                                            the broker they choose is familiar with export customs clearance
                                            procedures, as some customs brokers only provide import
                                            customs clearance services</li>
                                        <li><strong>d. Shipping Agents</strong> - A shipping agency is the
                                            designated
                                            agency responsible for handling shipments and cargo at ports
                                            and harbours worldwide on behalf of ship owners, managers,
                                            and charterers. Shipping agents usually take care of all the
                                            routine tasks of a shipping company quickly and efficiently.
                                            They ensure that essential supplies, crew transfers, customs
                                            documentation, and waste declarations are all arranged with the
                                            port authorities without delay.</li>
                                    </ul>
                                </div>
